library(shiny)
library(shinyFiles)

################################################################################
# Configuration options
################################################################################
port <- 1338
host <- "0.0.0.0"
maxRequestSize <- 30 * 1024^2 # 30 MiB
printSessionInfo <- TRUE

################################################################################
# Set Shiny options
################################################################################
options(shiny.host = host)
options(shiny.port = port)
options(shiny.maxRequestSize = maxRequestSize)

################################################################################
# Debug session
################################################################################
if (printSessionInfo) {
   print(sessionInfo())
}

################################################################################
# Start application
################################################################################
runApp(".")
